/*
%d is a format specifier used in printf.

It tells printf:

“Print the value as a decimal integer.”

Step by step:

printf("ineuron")

Prints: ineuron

Returns: 7 (because 7 characters were printed)

printf("%d", x);

%d says: print an integer

x is 7

So it prints: 7

*/




